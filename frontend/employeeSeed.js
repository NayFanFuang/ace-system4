export function buildProjectsFromExcel(existingProjects = []) {
  const existingCodes = new Set(existingProjects.map(p => p.project_code))
  const byCode = new Map()
  for (const row of EMPLOYEE_XLSX_ROWS) {
    if (!row.projectCode || existingCodes.has(row.projectCode)) continue
    if (!byCode.has(row.projectCode)) {
      byCode.set(row.projectCode, { ...row, headcount: 0 })
    }
    byCode.get(row.projectCode).headcount += 1
  }
  return Array.from(byCode.values()).map((row, index) => ({
    id: 500 + index,
    project_code: row.projectCode,
    project_name: row.projectName || row.projectCode,
    project_type: row.team === 'TE' ? 'TE' : row.team === 'RF' ? 'RF' : 'OTHER',
    status: 'ACTIVE',
    start_date: '2026-05-01',
    end_date: null,
    customer: { id: 900 + index, code: row.projectCode.slice(0, 3).toUpperCase(), name: row.projectName || row.projectCode },
    team: { code: row.team, name: row.team + ' Team' },
    headcount: row.headcount,
    site_count: 0,
  }))
}
